					<p style="text-align:justify;">Le Club propose des ateliers, bas&eacute;s sur la collaboration, l&rsquo;&eacute;change entre &eacute;l&egrave;ves. Ceux-ci peuvent s&rsquo;exercer à pr&eacute;senter leurs id&eacute;es, leur projets d&rsquo;entreprenariat au travers de <span class="color2">concours de pitchs</span>. </p> 
